import { z } from "zod";

export const generationRequestSchema = z.object({
  prompt: z.string().trim().min(10).max(2000),
});

export const generationResponseSchema = z.object({
  config: z.string().min(1),
  commands: z.array(z.string().min(1)).min(1),
  guidance: z.array(z.string().min(1)).min(1),
});

export type GenerationData = z.infer<typeof generationResponseSchema>;
export type RawGenerationData = {
  config: string | Record<string, unknown>;
  commands?: string[];
  guidance?: string[];
};

const modelResponseSchema = z.object({
  config: z.union([z.string().min(1), z.record(z.string(), z.unknown())]),
  commands: z.array(z.string().min(1)).optional(),
  guidance: z.array(z.string().min(1)).optional(),
});

const looseModelResponseSchema = z.object({
  config: z.union([z.string(), z.record(z.string(), z.unknown())]).optional(),
  configFileContent: z.string().optional(),
  wrangler: z.string().optional(),
  wranglerJsonc: z.string().optional(),
  commands: z.union([z.array(z.string()), z.string()]).optional(),
  terminalCommands: z.union([z.array(z.string()), z.string()]).optional(),
  guidance: z.union([z.array(z.string()), z.string()]).optional(),
  steps: z.union([z.array(z.string()), z.string()]).optional(),
  nextSteps: z.union([z.array(z.string()), z.string()]).optional(),
  changes: z.union([z.array(z.string()), z.string()]).optional(),
});

type NormalizedRequirements = {
  includeNode: boolean;
  includeD1: boolean;
  includeR2: boolean;
  includeKv: boolean;
  includeAi: boolean;
  includeBrowser: boolean;
  framework?: string;
};

function slugify(value: string) {
  const slug = value
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "")
    .slice(0, 40);

  return slug || "my-worker";
}

function uniqueName(base: string, suffix: string) {
  return `${base}-${suffix}`.replace(/--+/g, "-");
}

function hasKeyword(prompt: string, keywords: string[]) {
  return keywords.some((keyword) => prompt.includes(keyword));
}

function inferRequirements(prompt: string): NormalizedRequirements {
  const normalizedPrompt = prompt.toLowerCase();

  let framework: string | undefined = undefined;
  if (hasKeyword(normalizedPrompt, ["next.js", "nextjs"])) framework = "Next.js";
  else if (hasKeyword(normalizedPrompt, ["vue", "nuxt"])) framework = "Vue/Nuxt";
  else if (hasKeyword(normalizedPrompt, ["react", "remix"])) framework = "React";
  else if (hasKeyword(normalizedPrompt, ["hono"])) framework = "Hono";

  return {
    includeD1: hasKeyword(normalizedPrompt, ["d1", "database", "sqlite"]),
    includeR2: hasKeyword(normalizedPrompt, ["r2", "bucket", "object storage", "image storage"]),
    includeKv: hasKeyword(normalizedPrompt, ["kv", "cache", "namespace"]),
    includeAi: hasKeyword(normalizedPrompt, ["workers ai", "ai binding", "llm", "model"]),
    includeBrowser: hasKeyword(normalizedPrompt, ["browser"]),
    includeNode: hasKeyword(normalizedPrompt, ["next.js", "node", "npm", "package.json", "react", "nodejs_compat"]),
    framework,
  };
}

function buildConfig(options: {
  name: string;
  includeNode: boolean;
  includeD1: boolean;
  includeR2: boolean;
  includeKv: boolean;
  includeAi: boolean;
  includeBrowser: boolean;
}) {
  const lines = [
    "{",
    `  // Generated by Wrangler Wizard`,
    `  "name": "${options.name}",`,
    `  "compatibility_date": "2025-03-01",`,
  ];

  if (options.includeNode) {
    lines.push(`  "compatibility_flags": ["nodejs_compat"],`);
  }

  if (options.includeD1) {
    lines.push(
      "  \"d1_databases\": [",
      "    {",
      '      "binding": "DB",',
      '      "database_name": "my-database",',
      '      "database_id": "replace-with-d1-database-id"',
      "    }",
      "  ],"
    );
  }

  if (options.includeR2) {
    lines.push(
      '  "r2_buckets": [',
      "    {",
      '      "binding": "ASSETS",',
      '      "bucket_name": "my-assets"',
      "    }",
      "  ],"
    );
  }

  if (options.includeKv) {
    lines.push(
      '  "kv_namespaces": [',
      "    {",
      '      "binding": "CACHE",',
      '      "id": "replace-with-kv-namespace-id"',
      "    }",
      "  ],"
    );
  }

  if (options.includeAi) {
    lines.push(
      '  "ai": {',
      '    "binding": "AI"',
      "  },"
    );
  }

  if (options.includeBrowser) {
    lines.push(
      '  "browser": {',
      '    "binding": "BROWSER"',
      "  },"
    );
  }

  if (lines[lines.length - 1].endsWith(",")) {
    lines[lines.length - 1] = lines[lines.length - 1].slice(0, -1);
  }

  lines.push("}");

  return lines.join("\n");
}

function buildCommands(options: {
  baseName: string;
  includeD1: boolean;
  includeR2: boolean;
  includeKv: boolean;
}) {
  const commands = [`npm create cloudflare@latest ${safeCommandArg(options.baseName, "my-worker")}`, "npx wrangler deploy"];

  if (options.includeD1) {
    commands.unshift(`npx wrangler d1 create ${uniqueName(options.baseName, "db")}`);
  }

  if (options.includeR2) {
    commands.unshift(`npx wrangler r2 bucket create ${uniqueName(options.baseName, "assets")}`);
  }

  if (options.includeKv) {
    commands.unshift(`npx wrangler kv namespace create ${uniqueName(options.baseName, "cache")}`);
  }

  return commands;
}

function buildGuidance(options: {
  includeD1: boolean;
  includeR2: boolean;
  includeKv: boolean;
  includeNode: boolean;
  framework?: string;
}) {
  const steps = [
    options.framework
      ? `Run npm create cloudflare@latest <project-name> and select ${options.framework} from the templates.`
      : "Run npm create cloudflare@latest <project-name> and select your framework/template.",
    "Copy the generated wrangler.jsonc content into your project.",
  ];

  if (options.includeD1) {
    steps.push(
      "Run npx wrangler d1 create <database-name> and replace database_id with the returned UUID.",
      "Keep binding as DB and ensure database_name matches the D1 resource you created."
    );
  }

  if (options.includeR2) {
    steps.push(
      "Run npx wrangler r2 bucket create <bucket-name> and ensure bucket_name matches the created bucket."
    );
  }

  if (options.includeKv) {
    steps.push(
      "Run npx wrangler kv namespace create <namespace-name> and replace id with the returned namespace ID."
    );
  }

  if (options.includeNode) {
    steps.push("Keep compatibility_flags including nodejs_compat because Node APIs/packages were requested.");
  }

  steps.push("Run npx wrangler deploy after replacing all placeholder IDs.");

  return steps;
}

export function buildFallbackGeneration(prompt: string): GenerationData {
  const requirements = inferRequirements(prompt);
  const baseName = slugify(prompt);

  return {
    config: buildConfig({
      name: baseName,
      includeNode: requirements.includeNode,
      includeD1: requirements.includeD1,
      includeR2: requirements.includeR2,
      includeKv: requirements.includeKv,
      includeAi: requirements.includeAi,
      includeBrowser: requirements.includeBrowser,
    }),
    commands: buildCommands({
      baseName,
      includeD1: requirements.includeD1,
      includeR2: requirements.includeR2,
      includeKv: requirements.includeKv,
    }),
    guidance: buildGuidance({
      includeD1: requirements.includeD1,
      includeR2: requirements.includeR2,
      includeKv: requirements.includeKv,
      includeNode: requirements.includeNode,
      framework: requirements.framework,
    }),
  };
}

