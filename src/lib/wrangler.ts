function stripCodeFences(input: string) {
  return input
    .trim()
    .replace(/^```(?:json)?\s*/i, "")
    .replace(/\s*```$/i, "");
}

function extractFirstJsonObject(input: string) {
  let start = -1;
  let depth = 0;
  let inString = false;
  let escaped = false;

  for (let index = 0; index < input.length; index += 1) {
    const char = input[index];

    if (inString) {
      if (escaped) {
        escaped = false;
        continue;
      }

      if (char === "\\") {
        escaped = true;
        continue;
      }

      if (char === '"') {
        inString = false;
      }

      continue;
    }

    if (char === '"') {
      inString = true;
      continue;
    }

    if (char === "{") {
      if (depth === 0) {
        start = index;
      }

      depth += 1;
      continue;
    }

    if (char === "}") {
      if (depth === 0) {
        continue;
      }

      depth -= 1;

      if (depth === 0 && start !== -1) {
        return input.slice(start, index + 1);
      }
    }
  }

  return null;
}

function normalizeCommands(commands: string[] | string | undefined) {
  if (!commands) {
    return null;
  }

  if (Array.isArray(commands)) {
    return commands.map((command) => command.trim()).filter(Boolean);
  }

  return commands
    .split("\n")
    .map((command) => command.trim())
    .filter(Boolean);
}

function normalizeModelOutput(parsed: unknown) {
  const loose = looseModelResponseSchema.safeParse(parsed);

  if (!loose.success) {
    return null;
  }

  const rawConfig =
    loose.data.config ?? loose.data.configFileContent ?? loose.data.wrangler ?? loose.data.wranglerJsonc;
  const commands = normalizeCommands(loose.data.commands ?? loose.data.terminalCommands);
  const guidance = normalizeCommands(
    loose.data.guidance ?? loose.data.steps ?? loose.data.nextSteps ?? loose.data.changes
  );

  if (!rawConfig) {
    return null;
  }

  const config = typeof rawConfig === "string" ? rawConfig : JSON.stringify(rawConfig, null, 2);

  return modelResponseSchema.safeParse({
    config,
    commands: commands ?? [],
    guidance: guidance ?? [],
  });
}

export function parseModelResponse(text: string) {
  const cleanedText = stripCodeFences(text);

  const parseCandidates = [cleanedText, extractFirstJsonObject(cleanedText)].filter(
    (candidate): candidate is string => Boolean(candidate)
  );

  for (const candidate of parseCandidates) {
    try {
      const parsed = JSON.parse(candidate) as unknown;
      const normalized = normalizeModelOutput(parsed);

      if (normalized?.success) {
        return normalized;
      }
    } catch {
      // Keep trying remaining parse candidates.
    }
  }

  return modelResponseSchema.safeParse(null);
}

function stripJsonComments(input: string) {
  return input
    .replace(/\/\/.*$/gm, "")
    .replace(/\/\*[\s\S]*?\*\//g, "")
    .replace(/,\s*([}\]])/g, "$1");
}

function normalizeBindingArray(value: unknown, kind: "kv" | "d1" | "r2") {
  if (!Array.isArray(value)) {
    return [] as Array<Record<string, string>>;
  }

  return value
    .filter((entry): entry is Record<string, unknown> => Boolean(entry) && typeof entry === "object")
    .map((entry) => {
      if (kind === "kv") {
        const rawId =
          typeof entry.id === "string"
            ? entry.id
            : typeof entry.namespace_id === "string"
              ? entry.namespace_id
              : "";

        const normalizedId = /^[a-f0-9]{32}$/i.test(rawId)
          ? rawId
          : "replace-with-kv-namespace-id";

        return {
          binding:
            typeof entry.binding === "string"
              ? entry.binding
              : typeof entry.name === "string"
                ? entry.name
                : "CACHE",
          id: normalizedId,
        };
      }

      if (kind === "d1") {
        const rawDbName =
          typeof entry.database_name === "string"
            ? entry.database_name
            : typeof entry.name === "string"
              ? entry.name
              : "my-database";

        const rawDatabaseId =
          typeof entry.database_id === "string"
            ? entry.database_id
            : typeof entry.id === "string"
              ? entry.id
              : "";

        const normalizedDatabaseId =
          /^[a-f0-9]{32}$/i.test(rawDatabaseId) ||
          /^[a-f0-9]{8}-[a-f0-9]{4}-[1-5][a-f0-9]{3}-[89ab][a-f0-9]{3}-[a-f0-9]{12}$/i.test(rawDatabaseId)
            ? rawDatabaseId
            : "replace-with-d1-database-id";

        return {
          binding: typeof entry.binding === "string" ? entry.binding : "DB",
          database_name: safeCommandArg(rawDbName, "my-database"),
          database_id: normalizedDatabaseId,
        };
      }

      return {
        binding: typeof entry.binding === "string" ? entry.binding : "ASSETS",
        bucket_name:
          typeof entry.bucket_name === "string"
            ? entry.bucket_name
            : typeof entry.name === "string"
              ? entry.name
              : "my-assets",
      };
    });
}

function normalizeCommandsForWrangler(commands: string[]) {
  const normalized = commands
    .map((command) => command.trim())
    .filter(Boolean)
    .map((command) => {
      if (/^npm\s+create\s+/i.test(command) || /^pnpm\s+create\s+/i.test(command) || /^yarn\s+create\s+/i.test(command)) {
        return command;
      }

      if (/^npx\s+wrangler\s+(generate|init)\b/i.test(command) || /^wrangler\s+(generate|init)\b/i.test(command)) {
        return "npm create cloudflare@latest";
      }

      if (/^npx\s+wrangler\s+config\b/i.test(command) || /^wrangler\s+config\b/i.test(command)) {
        return "";
      }

      if (/^npm\s+(i|install)\s+(-g\s+)?wrangler/i.test(command)) {
        return "";
      }

      command = command.replace(/\bwrangler\s+publish\b/gi, "wrangler deploy");
      command = command.replace(/\bwrangler\s+kv:namespace\b/gi, "wrangler kv namespace");

      if (command.startsWith("wrangler ")) {
        return `npx ${command}`;
      }

      if (!command.startsWith("npx wrangler ")) {
        return `npx wrangler ${command}`;
      }

      return command;
    });

  return Array.from(new Set(normalized.filter(Boolean)));
}

function safeCommandArg(value: string, fallback: string) {
  const cleaned = value
    .toLowerCase()
    .replace(/[^a-z0-9-]+/g, "-")
    .replace(/^-+|-+$/g, "")
    .slice(0, 40);

  return cleaned || fallback;
}

function buildCanonicalCommandsFromConfig(normalized: Record<string, unknown>) {
  const commands: string[] = [];

  const workerName =
    typeof normalized.name === "string" ? safeCommandArg(normalized.name, "my-worker") : "my-worker";

  commands.push(`npm create cloudflare@latest ${workerName}`);

  const kvNamespaces = Array.isArray(normalized.kv_namespaces)
    ? normalized.kv_namespaces
    : [];

  for (const namespace of kvNamespaces) {
    if (!namespace || typeof namespace !== "object") {
      continue;
    }

    const entry = namespace as Record<string, unknown>;
    const baseName =
      typeof entry.binding === "string"
        ? entry.binding
        : typeof entry.id === "string"
          ? entry.id
          : "cache";

    commands.push(`npx wrangler kv namespace create ${safeCommandArg(baseName, "cache")}`);
  }

  const d1Databases = Array.isArray(normalized.d1_databases)
    ? normalized.d1_databases
    : [];

  for (const database of d1Databases) {
    if (!database || typeof database !== "object") {
      continue;
    }

    const entry = database as Record<string, unknown>;
    const dbName =
      typeof entry.database_name === "string"
        ? entry.database_name
        : typeof entry.binding === "string"
          ? entry.binding
          : "my-database";

    commands.push(`npx wrangler d1 create ${safeCommandArg(dbName, "my-database")}`);
  }

  const r2Buckets = Array.isArray(normalized.r2_buckets)
    ? normalized.r2_buckets
    : [];

  for (const bucket of r2Buckets) {
    if (!bucket || typeof bucket !== "object") {
      continue;
    }

    const entry = bucket as Record<string, unknown>;
    const bucketName =
      typeof entry.bucket_name === "string"
        ? entry.bucket_name
        : typeof entry.binding === "string"
          ? entry.binding
          : "my-assets";

    commands.push(`npx wrangler r2 bucket create ${safeCommandArg(bucketName, "my-assets")}`);
  }

  commands.push("npx wrangler deploy");

  return Array.from(new Set(normalizeCommandsForWrangler(commands)));
}

export function normalizeGenerationData(data: RawGenerationData, prompt: string): GenerationData {
  const requirements = inferRequirements(prompt);
  const fallback = buildFallbackGeneration(prompt);

  try {
    const configText = typeof data.config === "string" ? data.config : JSON.stringify(data.config, null, 2);
    const raw = JSON.parse(stripJsonComments(configText)) as Record<string, unknown>;
    const normalized: Record<string, unknown> = { ...raw };

    const nestedBindings =
      normalized.bindings && typeof normalized.bindings === "object"
        ? (normalized.bindings as Record<string, unknown>)
        : null;

    const kv = normalizeBindingArray(
      normalized.kv_namespaces ?? nestedBindings?.kv_namespaces,
      "kv"
    );
    const d1 = normalizeBindingArray(
      normalized.d1_databases ?? nestedBindings?.d1_databases,
      "d1"
    );
    const r2 = normalizeBindingArray(
      normalized.r2_buckets ?? nestedBindings?.r2_buckets,
      "r2"
    );

    delete normalized.bindings;

    normalized.name =
      typeof normalized.name === "string" && normalized.name.trim()
        ? normalized.name.trim()
        : "my-worker";
    normalized.compatibility_date = "2025-03-01";

    const flags = Array.isArray(normalized.compatibility_flags)
      ? normalized.compatibility_flags.filter((value): value is string => typeof value === "string")
      : [];

    if (requirements.includeNode && !flags.includes("nodejs_compat")) {
      flags.push("nodejs_compat");
    }

    if (!requirements.includeNode) {
      const filteredFlags = flags.filter((flag) => flag !== "nodejs_compat");
      flags.length = 0;
      flags.push(...filteredFlags);
    }

    if (flags.length > 0) {
      normalized.compatibility_flags = flags;
    } else {
      delete normalized.compatibility_flags;
    }

    if (requirements.includeKv || kv.length > 0) {
      normalized.kv_namespaces = kv.length > 0 ? kv : [{ binding: "CACHE", id: "replace-with-kv-namespace-id" }];
    } else {
      delete normalized.kv_namespaces;
    }

    if (requirements.includeD1 || d1.length > 0) {
      normalized.d1_databases =
        d1.length > 0
          ? d1
          : [{ binding: "DB", database_name: "my-database", database_id: "replace-with-d1-database-id" }];
    } else {
      delete normalized.d1_databases;
    }

    if (requirements.includeR2 || r2.length > 0) {
      normalized.r2_buckets = r2.length > 0 ? r2 : [{ binding: "ASSETS", bucket_name: "my-assets" }];
    } else {
      delete normalized.r2_buckets;
    }

    const canonicalCommands = buildCanonicalCommandsFromConfig(normalized);
    const aiCommands = normalizeCommandsForWrangler(data.commands ?? []);

    const finalCommands =
      canonicalCommands.length > 0 ? canonicalCommands : aiCommands.length > 0 ? aiCommands : fallback.commands;

    const canonicalGuidance = buildGuidance({
      includeD1: requirements.includeD1 || Array.isArray(normalized.d1_databases),
      includeR2: requirements.includeR2 || Array.isArray(normalized.r2_buckets),
      includeKv: requirements.includeKv || Array.isArray(normalized.kv_namespaces),
      includeNode: requirements.includeNode,
      framework: requirements.framework,
    });

    const finalGuidance = Array.from(new Set(canonicalGuidance));

    return {
      config: JSON.stringify(normalized, null, 2),
      commands: finalCommands,
      guidance: finalGuidance.length > 0 ? finalGuidance : fallback.guidance,
    };
  } catch {
    return {
      config: fallback.config,
      commands: normalizeCommandsForWrangler((data.commands ?? []).length > 0 ? (data.commands ?? []) : fallback.commands),
      guidance: fallback.guidance,
    };
  }
}